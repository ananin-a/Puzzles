                self .health = 0
            }
        }
    }
    var name: String
    var face: Character
    
    var xCoordinate = 1 {
        didSet {
            switch xCoordinate {
            case 0:
                xCoordinate = oldValue
                self.health -= self .damage
            case Room.width + 1:
                xCoordinate = oldValue
                self.health -= self .damage
            default:
                print()
            }
        }
    }
    
    var yCoordinate = 1 {
        didSet {
            switch yCoordinate {
            case 0:
                yCoordinate = oldValue
                self.health -= self .damage
            case Room.height + 1:
                yCoordinate = oldValue
                self.health -= self .damage
            default:
                print()
            }
        }
    }
    
    enum MovieDirection {
        case up, down, left, right
    }
    
    mutating func checkGoToCesspool() {
        if self .xCoordinate == Cesspool.xCoordinate && self .yCoordinate == Cesspool.yCoordinate {
            self .health = 0
        }
    }
    
    func checkCellOccupied() -> Bool {
        if self.xCoordinate == Box.xCoordinate && self .yCoordinate == Box.yCoordinate {
            return false
        }
        return true
    }
    
    mutating func moveTo(_ movie: MovieDirection) {
        if self .face != self .death && checkCellOccupied() {
            switch movie {
            case .up:
                yCoordinate -= 1
                checkGoToCesspool()
                if yCoordinate == Box.yCoordinate && xCoordinate == Box.xCoordinate {
                    Box.yCoordinate -= 1
                    box.checkFinish()
                }
            case .down:
                yCoordinate += 1
                checkGoToCesspool()
                if yCoordinate == Box.yCoordinate && xCoordinate == Box.xCoordinate {
                    Box.yCoordinate += 1
                    box.checkFinish()
                }
            case .left:
                xCoordinate -= 1
                checkGoToCesspool()
                if xCoordinate == Box.xCoordinate && yCoordinate == Box.yCoordinate {
                    Box.xCoordinate -= 1
                    box.checkFinish()
                }
            case .right:
                xCoordinate += 1
                checkGoToCesspool()
                if xCoordinate == Box.xCoordinate && yCoordinate == Box.yCoordinate {
                    Box.xCoordinate += 1
                    box.checkFinish()
                    
                }
            }
        } else {
            print()
            print(" üíÄ üíÄ üíÄ üíÄ>>>GAME OVER<<<üíÄ üíÄ üíÄ üíÄ")
        }
    }
}

